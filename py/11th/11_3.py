# Определяем ядро интегрального уравнения


def K(x, s):
    return np.exp(x - s)

# Определяем функцию для нахождения приближенного решения


def solve(n):
    h = (b-a)/n
    x = np.linspace(a, b, n+1)
    y = np.zeros(n+1)
    for i in range(1, n+1):
        y[i] = y[i-1] + h * (f(x[i-1]) + K(x[i-1], x[i-1])*y[i-1])/2
    return x, y


# Находим приближенное решение и выводим результаты для каждого значения n
for n in n_values:
    x, y = solve(n)
